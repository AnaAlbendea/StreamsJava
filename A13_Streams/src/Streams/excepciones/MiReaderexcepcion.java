package Streams.excepciones;

public class MiReaderexcepcion extends RuntimeException{

	public MiReaderexcepcion() {
		super();
		
	}

	public MiReaderexcepcion(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public MiReaderexcepcion(String message, Throwable cause) {
		super(message, cause);
		
	}

	public MiReaderexcepcion(String message) {
		super(message);
		
	}

	public MiReaderexcepcion(Throwable cause) {
		super(cause);
		
	}
	
	
}
